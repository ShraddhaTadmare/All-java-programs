package loopAssignment;

public class DisplayOddNumbersExample1 {
	public static void main(String args[])   
	{  
		int n = 521;
        
        //print all odd numbers <=n 
        int i=229;
        while (i<=n) {
            if(i%2==1) {
                System.out.print(i+"  ");
            }
            i++;
        }


}
}
